package ImageViewer;

public class ImageInfo {

	public String path;
	public String author;
	public String location;
	public String date;
	public String tag;
	
	public ImageInfo(String path, String author, String location, String date, String tag)
	{
		this.path = path;
		this.author = author;
		this.location = location;
		this.date = date;
		this.tag = tag;
	}
}
